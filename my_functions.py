def add(a, b):
    return a + b

def subtract(a, b):
    return a - b

def multiply(a, b):
    return a * b

def divide(a, b):
    return a / b

def is_even(n):
    return n % 2 == 0

def is_odd(n):
    return n % 2 != 0

def max_number(a, b):
    return a if a > b else b

def min_number(a, b):
    return a if a < b else b

def reverse_string(text):
    return text[::-1]

def to_upper(text):
    return text.upper()
